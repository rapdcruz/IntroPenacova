package com.example.intropenacova.model;

public class ElementosInscritos {

    String Nome;
    String Equipa;

    public ElementosInscritos(String nome, String equipa) {
        Nome = nome;
        Equipa = equipa;

    }


    public String getNome() {
        return Nome;
    }

    public void setNome(String nome) {
        Nome = nome;
    }

    public String getEquipa() {
        return Equipa;
    }

    public void setEquipa(String equipa) {
        Equipa = equipa;
    }





}
